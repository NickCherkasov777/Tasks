# areas.py

import math

def rectangle_area(a, b):
    """Calculates the area of a rectangle."""
    return a * b

def triangle_area(h, a):
    """Calculates the area of a triangle."""
    return 0.5 * h * a

def circle_area(r):
    """Calculates the area of a circle."""
    return math.pi * math.pow(r, 2)


